func linearToALaw(sample int16) byte {
	sign := byte((^sample)>>8) & 0x80
	if sign == 0 {
		sample = -sample
	}

	if sample > cClip {
		sample = cClip
	}

	var compressedByte byte
	if sample >= 256 {
		exponent := aLawCompressTable[(sample>>8)&0x7F]
		mantissa := byte(sample>>(exponent+3)) & 0x0F
		compressedByte = (exponent << 4) | mantissa
	} else {
		compressedByte = byte(sample >> 4)
	}
	compressedByte ^= sign ^ 0x55
	return compressedByte
}

func LinearToALaw(samples []byte, alaws []byte) error {
	if len(samples) > 2*len(alaws) {
		return errors.New("data size too small")
	}
	for i := 0; i < len(samples); i += 2 {
		alaws[i/2] = linearToALaw(int16(samples[i]) | int16(samples[i+1])<<8)
	}
	return nil
}

